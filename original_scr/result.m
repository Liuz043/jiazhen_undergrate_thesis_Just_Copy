%% 检查不同破裂点口径下的可分配剩余

% ==============================
% 1. 以合作前实际收益为破裂点
% ==============================
% 风电实际收益：sf
% 光伏实际收益：sg
% 电制氢非合作效用：Uh02 = -value(Uh0)
% 电制氢合作基础效用：Wh2 = -合作成本

surplus_actual = ...
    (Wwt2 - sf) ...
  + (Wpv2 - sg) ...
  + (Wh2 - Uh02);

disp('以实际合作前收益为破裂点时的可分配剩余：');
disp(surplus_actual);


% ==============================
% 2. 以合作前理想收益为破裂点
% ==============================
% 风电理想收益：Uwt02 = -f01
% 光伏理想收益：Upv02 = -f02
% 电制氢非合作效用：Uh02 = -value(Uh0)

surplus_ideal = ...
    (Wwt2 - Uwt02) ...
  + (Wpv2 - Upv02) ...
  + (Wh2 - Uh02);

disp('以理想合作前收益为破裂点时的可分配剩余：');
disp(surplus_ideal);